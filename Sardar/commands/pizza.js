const axios = require('axios');
const fs = require('fs-extra');
const path = require('path');

module.exports = {
  config: {
    credits: "SARDAR RDX",
    name: 'pizza',
    aliases: ['piza', 'piazza', 'pizz'],
    description: 'Pizza ki tasty image bhejo.',
    usage: 'pizza',
    category: 'Food',
    adminOnly: false,
    prefix: true
  },

  async run({ event, send }) {
    const images = [
      "https://i.ibb.co/spXqrkZ7/c68e92889bcd.jpg",
      "https://i.ibb.co/BHWs6KVr/c62dfd0b8de4.jpg",
      "https://i.ibb.co/PsS6pMGX/def448a0201b.jpg",
      "https://i.ibb.co/k25ZZMMv/7843c13ec84a.jpg"
    ];

    const randomImg = images[Math.floor(Math.random() * images.length)];
    const cacheDir = path.join(__dirname, 'cache');
    fs.ensureDirSync(cacheDir);
    const imgPath = path.join(cacheDir, `pizza_${Date.now()}.jpg`);

    try {
      const response = await axios.get(randomImg, { responseType: 'arraybuffer', timeout: 15000 });
      fs.writeFileSync(imgPath, Buffer.from(response.data));

      await send.reply({
        body: `╭─── « 🍕 PIZZA » ───⟡\n│\n│ 🍕 Ye lo hot pizza\n│    aapke liye! 😋\n│\n│ 👑 SARDAR RDX BOT\n╰───────────────⟡`,
        attachment: fs.createReadStream(imgPath)
      });
      try { fs.unlinkSync(imgPath); } catch {}
    } catch (err) {
      try { fs.unlinkSync(imgPath); } catch {}
      return send.reply(`╭─── « ❌ ERROR » ───⟡\n│\n│ 😔 Image nahi mili!\n│ Dobara try karo.\n│\n╰───────────────⟡`);
    }
  }
};
